package interfaces;

// ▶ Importaciones ─────────────────────────────────────────────────────────────────────────────────────────────────────
import java.util.List;

public interface INodo<T extends Comparable<T>> {

    // ▶ Getters ───────────────────────────────────────────────────────────────────────────────────────────────────────
    public T getDato()                  ;
    public boolean getVisitado()        ;
    public List<INodo<T>> getVecinos()  ;

    // ▶ Setters ───────────────────────────────────────────────────────────────────────────────────────────────────────
    public void setDato(T dato)         ;

    // ▶ Otros ─────────────────────────────────────────────────────────────────────────────────────────────────────────
    public void agregarVecino(INodo<T> nodo)    ;

// ─────────────────────────────────────────────────────────────────────────────────────────────────────────────────────
}